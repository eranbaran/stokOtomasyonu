    private void urun_guncellestirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_urun_guncellestirActionPerformed
       String marka = marka_alani.getText();
        String fiyat = fiyat_alani.getText();
        String stok = stokSayisi_alani.getText();
        String magaza = magazaismi_alani.getText();
        String kategori = kategori_alani.getSelectedItem().toString();
        
        int selectedRow = urunTablosu.getSelectedRow();
        if(selectedRow == -1){
            if(model.getRowCount()== 0){
                mesaj_alani.setText("Ürünler tablosu boş. Güncelleme işlemi başarısız oldu!");
                urunTablosuBosAudio();
            }else{
                mesaj_alani.setText("Lütfen güncellenecek bir ürün seçiniz !");
                guncelLenecekUrunAudio();
            }
        }else{
            
            int id = (int) model.getValueAt(selectedRow, 0);
            
            islemler.urunGuncelle(id,kategori,marka,fiyat,stok,magaza);
            urunGoruntule();
            mesaj_alani.setText("Ürün tablonuz başarıyla güncellendi");
            tebriklerAudio();
            girilenleriTemizle();
        }
    }//GEN-LAST:event_urun_guncellestirActionPerformed

    private void urun_silActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_urun_silActionPerformed
       mesaj_alani.setText("");
       int selectedRow = urunTablosu.getSelectedRow();
       if(selectedRow == -1){
           
           if(model.getRowCount() == 0){
               mesaj_alani.setText("Ürün tablosu şuanda boş. Silme işlemi başarısız oldu !");
               silmeBosAudio();
               
           }else{
               mesaj_alani.setText("Lütfen silinecek bir ürün seçiniz !");
               silUrunSecAudio();
           }
           
       }else{
           int id =(int) model.getValueAt(selectedRow, 0);
           islemler.urunuSil(id);
           urunGoruntule();
           mesaj_alani.setText("Seçtiğiniz ürün başarıyla silindi !");
           tebriklerAudio();
           girilenleriTemizle();
       }
    }//GEN-LAST:event_urun_silActionPerformed

