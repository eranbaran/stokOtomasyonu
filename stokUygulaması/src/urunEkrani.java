    public void urunGoruntule(){
        model.setRowCount(0);
        ArrayList<urun> urunler =new ArrayList<urun>();
        urunler = islemler.urunleriGetir();
        if(urunler != null){
            for(urun Urun : urunler){
                Object [] eklenecek = {Urun.getId(),Urun.getKategori(),Urun.getUrunİsmi(),Urun.getFiyat(),Urun.getStokSayisi(),Urun.getMagazaİsmi()};
                model.addRow(eklenecek);
            }
        }
    }
